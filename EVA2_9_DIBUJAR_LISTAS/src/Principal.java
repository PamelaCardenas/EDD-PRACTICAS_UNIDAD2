        Point pCoor = (evt.getPoint());
        Graphics gpMiDibu = canvas1.getGraphics();
        gpMiDibu.drawRect(pCoor.x -5, pCoor.y-5, 10, 10);
        
        list.agregarNodo(new Nodo(pCoor));
        
        
